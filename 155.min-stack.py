# @lc code=start
class MinStack:

    def __init__(self):
        self.stk1 = []
        self.stk2 = [float('inf')]

    def push(self, val: int) -> None:
        self.stk1.append(val)
        self.stk2.append(min(self.stk2[-1], val))

    def pop(self) -> None:
        self.stk1.pop()
        self.stk2.pop()

    def top(self) -> int:
        return self.stk1[-1]

    def getMin(self) -> int:
        return self.stk2[-1]


